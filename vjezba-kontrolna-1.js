function najmladiStudent(){
  let najmladi = {
    ime: "",
    datum_rodenja: "0"
  }
  for(let student of studenti){
    if(student.datum_rodenja > najmladi.datum_rodenja){
      najmladi.ime = student.ime,
      najmladi.datum_rodenja = student.datum_rodenja
    }
  }
  return najmladi

}

